int dx1[] = {0,0,-1,1,-1,1}, dy1[] = {1,-1,1,1,0,0}, 
	dx0[] = {0,0,-1,1,-1,1}, dy0[] = {1,-1,0,0,-1,-1};

int Islands::beachLength(vector <string> kingdom) {
	int N = kingdom.size(), M = kingdom[0].length();    	
	int res = 0;
    for (int i = 0; i < N; i ++) {
        for (int j = 0; j < M; j ++) 
            if (kingdom[i][j] == '#') {
                for (int k = 0; k < 6; k ++) {
                    int i1, j1;
                
                    if (i % 2 == 0) {
                        i1 = i + dx0[k];
                        j1 = j + dy0[k];
                    } 
                    else {
                        i1 = i + dx1[k];
                        j1 = j + dy1[k];
                    }
                
                    if (i1 < 0 || j1 < 0 || i1 >= N || j1 >= M) continue;
                                
                    if (kingdom[i1][j1] == '.') res ++;
            }
        }
    }
	
	return res;
}



