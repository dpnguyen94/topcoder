class VacationTime {
public:
	int bestSchedule(int N, int K, vector <int> workingDays);
};



int VacationTime::bestSchedule(int N, int K, vector <int> workingDays) {
	int n = workingDays.size();
    bool wdays[1010];
    memset(wdays,0,sizeof(wdays));
    
    for (int i = 0; i < n; i ++) wdays[workingDays[i]] = 1;
    
    int res = 1000000000;
    for (int i = 1; i <= N-K+1; i ++) {
        int cnt = 0;
        for (int j = i; j <= i+K-1; j ++) {
            if (wdays[j]) cnt ++;
        }
        if (cnt < res) res = cnt;
    }
    
    return res;
}



