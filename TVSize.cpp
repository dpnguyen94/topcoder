vector <int> TVSize::calcSize(int d, int h, int w) {
	double tmp = (double)w / h;
	double H = sqrt(d * d / ((tmp * tmp) + 1));
	double W = sqrt(d * d - H * H);
	
	vi res;
	res.pb((int)(H + 1e-10));
	res.pb((int)(W + 1e-10));
	
	return res;
}


