class MicrowaveSelling {
public:
	int mostAttractivePrice(int minPrice, int maxPrice);
};

int calc(int x) {
	int c = 0;
	while (x % 10 == 9) {
		c ++;
		x /= 10;
	}
	
	return c;
}

int MicrowaveSelling::mostAttractivePrice(int minPrice, int maxPrice) {
	int c, cnt = 0, res = 0;
	FOR(i,minPrice,maxPrice) {
		c = calc(i);
		if (c > cnt) {
			cnt = c;
			res = i;
		} else if (c == cnt && i > res) res = i;
	}
	
	return res;
}


